// leaderboard flags the configs and trials they belong to.
export function leaderboardFromTrials(trials, { title = "Leaderboard", hackIds = [] } = {}) {
  return { title, note: trials.note, rows: trials.configs, hackIds };
}

// Auto-pick the strongest N distinct trials from a trial set to showcase in the
// trajectory viewer. Used by tasks that have no live artifact, so we don't have
// to hand-author a per-task list across 20 tasks.
export function showcaseFromTrials(trials, n = 6) {
  const all = trials.configs.flatMap((c) => c.trials);
  const sorted = [...all].sort(
    (a, b) => b.partial - a.partial || b.correctness - a.correctness
  );
  const out = [];
  const seen = new Set();
  for (const t of sorted) {
    if (seen.has(t.id)) continue;
    seen.add(t.id);
    out.push({
      id: t.id,
      trial: t.trial,
      agent: t.agent,
      model: t.model,
      tokens: t.tokens,
      cost: t.cost,
      trajectoryUrl: t.trajectoryUrl,
    });
    if (out.length >= n) break;
  }
  return { trials: out };
}

// Hand-curated showcase: pick specific trials by id, in order, and attach an
// optional short `tag` badge (e.g. "Long-horizon run" / "Reward hack"). Used
// when the story we want to tell isn't just "the top scorers".
export function showcaseByIds(trials, items) {
  const byId = new Map(
    trials.configs.flatMap((c) => c.trials).map((t) => [t.id, t])
  );
  const out = [];
  for (const item of items) {
    const t = byId.get(item.id);
    if (!t) continue;
    out.push({
      id: t.id,
      trial: t.trial,
      agent: t.agent,
      model: t.model,
      tokens: t.tokens,
      cost: t.cost,
      trajectoryUrl: t.trajectoryUrl,
      tag: item.tag,
    });
  }
  return { trials: out };
}

